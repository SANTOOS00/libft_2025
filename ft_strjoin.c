#include "libft.h"

char	*ft_strjoin(char const *s1, char const *s2)
{
	char	*str;
	int		s1_len;
	int		s2_len;

	if (!s1 || !s2)
		return (NULL);
	s1_len = ft_strlen(s1);
	s2_len = ft_strlen(s2);
	str = malloc(sizeof(char) * (s1_len + s2_len + 1));
	if (!str)
		return (NULL);
	str[0] = '\0';
	ft_strlcat(str, s1, s1_len + s2_len + 1);
	ft_strlcat(str, s2, s1_len + s2_len + 1);
	return (str);
}
